package nc.vo.cy.creditcontract.pojo;

public class CContractCreateParamPojo {
	
	
	private String idcard;// 学生身份证号
	
	private String paymodel;// 付款模式
	
	private String custompayamount;// 自定义付款的金额
	
	
	public String getIdcard() {
		return idcard;
	}
	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}
	public String getPaymodel() {
		return paymodel;
	}
	public void setPaymodel(String paymodel) {
		this.paymodel = paymodel;
	}
	public String getCustompayamount() {
		return custompayamount;
	}
	public void setCustompayamount(String custompayamount) {
		this.custompayamount = custompayamount;
	}
	
	
	
	

}
