package nc.vo.cy.pojo;

public class DefdocPOJO {

	// 大区编码
	private String dqcode;
	// 省份编码
	private String sfcode;
	// 学校编码（客户）
	private String xxcode;

	public String getDqcode() {
		return dqcode;
	}

	public void setDqcode(String dqcode) {
		this.dqcode = dqcode;
	}

	public String getSfcode() {
		return sfcode;
	}

	public void setSfcode(String sfcode) {
		this.sfcode = sfcode;
	}

	public String getXxcode() {
		return xxcode;
	}

	public void setXxcode(String xxcode) {
		this.xxcode = xxcode;
	}
}
